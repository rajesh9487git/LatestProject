package JavaPrograms;

public class TernaryOperator {

	public static void main(String[] args) {
		
		int a=6;
		int b=4;
		int j=0;
		
		// "?:" is the ternary operator, before "?" is the condition, if it is true 1st statement is executed , if it is false 2nd statement is executed. 
		// Statements are separated by ":" 
		j=a>b?1:2;
		
		System.out.println(j);
		
	

	}

}
