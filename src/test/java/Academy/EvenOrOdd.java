package Academy;

public class EvenOrOdd {

	public static void main(String[] args) {
		
		// even no is something that gives remainder 0 when dived by 2, otherwise it is an odd no. to find the remainder use modules operator '%'
		
		int givenno=20;
		
		if(givenno%2==0) {
			
			System.out.println("Given no is a even no");
		} else {
			
			System.out.println("Given no is an odd no");
			
			
		}
		
	}

}
